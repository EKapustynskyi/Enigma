class CodeWars {

    func scanner(_ qrcode: [[Int]]) -> String {

        // Word size
        let wordSize = [
            applyMask(18, 20) ? getInvByte(qrcode[18][20]) : String(qrcode[18][20]),
            applyMask(18, 19) ? getInvByte(qrcode[18][19]) : String(qrcode[18][19]),
            applyMask(17, 20) ? getInvByte(qrcode[17][20]) : String(qrcode[17][20]),
            applyMask(17, 19) ? getInvByte(qrcode[17][19]) : String(qrcode[17][19]),
            applyMask(16, 20) ? getInvByte(qrcode[16][20]) : String(qrcode[16][20]),
            applyMask(16, 19) ? getInvByte(qrcode[16][19]) : String(qrcode[16][19]),
            applyMask(15, 20) ? getInvByte(qrcode[15][20]) : String(qrcode[15][20]),
            applyMask(15, 19) ? getInvByte(qrcode[15][19]) : String(qrcode[15][19])
        ]

        var message = [String]()

        // Helper function to add character bits to the message
        func addCharBits(_ coordinates: [(Int, Int)]) {
            let bits = coordinates.map { (coord: (Int, Int)) -> String  in
                let (x, y) = coord
                return applyMask(x, y) ? getInvByte(qrcode[x][y]) : String(qrcode[x][y])
            }
            message.append(bits.joined())
        }

        // Define coordinates for each character based on original pattern
        addCharBits([(14, 20), (14, 19), (13, 20), (13, 19), (12, 20), (12, 19), (11, 20), (11, 19)]) // char1
        addCharBits([(10, 20), (10, 19), (9, 20), (9, 19), (9, 18), (9, 17), (10, 18), (10, 17)]) // char2
        addCharBits([(11, 18), (11, 17), (12, 18), (12, 17), (13, 18), (13, 17), (14, 18), (14, 17)]) // char3
        addCharBits([(15, 18), (15, 17), (16, 18), (16, 17), (17, 18), (17, 17), (18, 18), (18, 17)]) // char4
        addCharBits([(19, 18), (19, 17), (20, 18), (20, 17), (20, 16), (20, 15), (19, 16), (19, 15)]) // char5
        addCharBits([(18, 16), (18, 15), (17, 16), (17, 15), (16, 16), (16, 15), (15, 16), (15, 15)]) // char6
        addCharBits([(14, 16), (14, 15), (13, 16), (13, 15), (12, 16), (12, 15), (11, 16), (11, 15)]) // char7
        addCharBits([(10, 16), (10, 15), (9, 16), (9, 15), (9, 14), (9, 13), (10, 14), (10, 13)]) // char8

        // Convert binary message to characters
        let wordSizeValue = Int(wordSize.joined(), radix: 2) ?? 0
        var resultWordAsByteArray = [UInt8](repeating: 0, count: wordSizeValue)

        for i in 0..<wordSizeValue {
            if let byte = UInt8(message[i], radix: 2) {
                resultWordAsByteArray[i] = byte
            }
        }
        return String(bytes: resultWordAsByteArray, encoding: .utf8) ?? ""
    }

    func applyMask(_ coordinateX: Int, _ coordinateY: Int) -> Bool {
        return (coordinateX + coordinateY) % 2 == 0
    }

    func getInvByte(_ nonInvertedByte: Int) -> String {
        return String(nonInvertedByte ^ 1)
    }
}

